def print_sum(a, b):
    """Functions printing the sum of two numbers and returns True.
    
    Parameters
    ----------
    a : int or float
        first addend
    b : int or float
        second addend
    """
    _sum = a + b
    print(f"sum of {a:.2f} + {b:.2f} = {_sum:.2f}")

    return True
